                                <p class="mbr-text mbr-fonts-style display-7"><strong>TUVALU</strong>, formerly Ellice Islands, country in the west-central Pacific Ocean. It is composed of nine small coral islands scattered in a chain lying approximately northwest to southeast over a distance of some 420 miles (676 km). The de facto capital is the village of Vaiaku, where most government offices are located. It is on Fongafale islet, a constituent part of Funafuti Atoll. Together with what is now Kiribati (formerly the Gilbert Islands), Tuvalu formed the British Gilbert and Ellice Islands Colony before separately gaining its independence in 1978.
                                            <br>
                                            <br><strong>LANDS</strong> - The group includes both atolls and reef islands. The atolls—Nanumea, Nui, Nukufetau, Funafuti, and Nukulaelae—have islets encircling a shallow lagoon; the reef islands—Nanumanga, Niutao, Vaitupu, and Niulakita—are compact with a fringing reef. The islands are low-lying, most being 13 to 16 feet (4 to 5 metres) above sea level. There are no rivers; rain catchment and wells provide the only fresh water. Rainfall averages 100 inches (2,500 mm) in the north and 125 inches (3,175 mm) in the south. The prevailing winds are southeast trades; westerly storms occur from November to February. Daytime temperatures range from 80 to 85 °F (27 to 29 °C). Because the soils are porous, agriculture is limited. Coconut palms thrive, and breadfruit trees, pandanus, taro, and bananas are grown. Pigs and chickens are raised, and seabirds, fish, and shellfish are caught for food. The islands increasingly depend on imported food.
                                            <br>
                                            <br><strong>PEOPLE</strong> - The Tuvaluans are Polynesian, and their language, Tuvaluan, is closely related to Samoan. Nui, however, was heavily settled in prehistoric times by Micronesians from the Gilbert Islands (now Kiribati). English is taught in the schools and widely used. The vast majority of the population belongs to the Church of Tuvalu (the former Ellice Islands Protestant Church). Although most people live on the outer islands in extended family households clustered into villages, about one-third of the population lives on Funafuti, the centre of government and commerce. Population growth has been slowed by family planning; life expectancy at birth is about 60 years. About 10 percent of the population lives overseas, either pursuing education, working in the Nauru phosphate industry, or working on merchant ships.
                                            <br>
                                            <br><strong>ECONOMY</strong> - Most Tuvaluans are subsistence farmers and are aided by remittances from relatives working overseas. A small quantity of copra is produced for export, the sale of stamps accounts for modest earnings, and fees are collected from foreign fishing fleets, but the country depends heavily on foreign aid. It imports most of its food, fuel, and manufactured goods. Fiji, Australia, New Zealand, and Japan are among the country’s major trade partners. Retailing is handled by community-based cooperative societies. Tuvalu uses Australian currency but also issues its own coinage. There is a single bank, a joint government-commercial venture. Tuvalu has air links with Kiribati and Fiji; for international shipping, it depends on irregular regional services. Seaplanes have been used for interisland travel, but generally the outer islands depend on a single government vessel. Motorcycles are common on Funafuti, but there are few automobiles.
                                            <br>
                                            <br><strong>CULTURAL LIFE</strong> - The Tuvaluan lifestyle has been Westernized to an extent, but Western-style amenities are few. Only Funafuti has a regular electricity supply; the government publishes a brief news sheet, but there is no newspaper; a few motion pictures are shown; satellite television service is available only by subscription; and there is only a single radio station. Most Tuvaluans live in villages of a few hundred people, tend their gardens, and fish from handcrafted canoes. Traditional music and dancing still enjoy a strong following, along with Western forms. Volleyball, football (soccer), and cricket are popular. Tuvaluan life, despite modernization, still rests on a firm traditional base that emphasizes the importance of community consensus and identity.&nbsp;<br></p>
